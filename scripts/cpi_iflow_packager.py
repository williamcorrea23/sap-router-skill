def main():
    parser = argparse.ArgumentParser(
        description="CPI iFlow Packager — create, validate, extract SAP CPI iFlow ZIPs"
    )
    sub = parser.add_subparsers(dest="command", required=True)

    # create
    c = sub.add_parser("create", help="Create iFlow ZIP from source files")
    c.add_argument("--name", "-n", required=True, help="iFlow name (Bundle-SymbolicName)")
    c.add_argument("--flow", "-f", help="flow.xml file path")
    c.add_argument("--scripts", nargs="*", help="Groovy script files")
    c.add_argument("--mappings", nargs="*", help="XSLT/MMap mapping files")
    c.add_argument("--resources", help="Resources directory (EDI schemas, WSDLs)")
    c.add_argument("--vmaps", nargs="*", help="Value mapping .vmap files")
    c.add_argument("--parameters", help="parameters.prop file")
    c.add_argument("--security", nargs="*", help="Security artifact files")
    c.add_argument("--output", "-o", help="Output ZIP path (default: <name>.zip)")

    # template
    t = sub.add_parser("template", help="Generate starter iFlow ZIP template")
    t.add_argument("--name", "-n", required=True, help="iFlow name")
    t.add_argument("--output", "-o", help="Output ZIP path")

    # validate
    v = sub.add_parser("validate", help="Validate iFlow ZIP structure")
    v.add_argument("--input", "-i", required=True, help="iFlow ZIP to validate")

    # extract
    e = sub.add_parser("extract", help="Extract iFlow ZIP to directory")
    e.add_argument("--input", "-i", required=True, help="iFlow ZIP file")
    e.add_argument("--output", "-o", required=True, help="Output directory")

    # list
    l = sub.add_parser("list", help="List contents of iFlow ZIP")
    l.add_argument("--input", "-i", required=True, help="iFlow ZIP file")

    args = parser.parse_args()

    dispatch = {
        "create":   lambda: cmd_create(args),
        "template": lambda: cmd_template(args),
        "validate": lambda: cmd_validate(args),
        "extract":  lambda: cmd_extract(args),
        "list":     lambda: cmd_list(args),
    }

    return dispatch[args.command]()


if __name__ == "__main__":
    sys.exit(main())
