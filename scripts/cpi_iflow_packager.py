VALID_EXTENSIONS = {
    ".groovy": "script/",
    ".xslt": "mapping/",
    ".mmap": "mapping/",
    ".xsd": "resources/",
    ".wsdl": "resources/",
    ".vmap": "value-mapping/",
    ".prop": ".",
    ".xml": ".",
}

REQUIRED_FILES = [
    "META-INF/MANIFEST.MF",
    "src/main/resources/flow.xml",
]


# ---------------------------------------------------------------------------
# Commands
# ---------------------------------------------------------------------------

def cmd_create(args):
    """Create an iFlow ZIP from individual source files."""
    name = args.name
    output = args.output or f"{name}.zip"
    base = Path("src/main/resources")

    with zipfile.ZipFile(output, "w", zipfile.ZIP_DEFLATED) as zf:
        # 1. MANIFEST.MF
        manifest = MANIFEST_TEMPLATE.format(
            name=name,
            date=datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
        )
        zf.writestr("META-INF/MANIFEST.MF", manifest)

        # 2. flow.xml
        if args.flow:
            zf.write(args.flow, str(base / "flow.xml"))
        else:
            zf.writestr(str(base / "flow.xml"), FLOW_TEMPLATE.format(name=name))

        # 3. Groovy scripts
        for script in (args.scripts or []):
            dest = base / "script" / os.path.basename(script)
            zf.write(script, str(dest))

        # 4. Mappings (XSLT, MMap)
        for mapping in (args.mappings or []):
            ext = os.path.splitext(mapping)[1]
            subdir = "mapping" if ext in (".xslt", ".mmap") else "resources"
            dest = base / subdir / os.path.basename(mapping)
            zf.write(mapping, str(dest))

        # 5. Resources directory
        if args.resources:
            res_path = Path(args.resources)
            for f in res_path.rglob("*"):
                if f.is_file():
                    rel = f.relative_to(res_path)
                    zf.write(str(f), str(base / "resources" / rel))

        # 6. Value mappings
        for vmap in (args.vmaps or []):
            dest = base / "value-mapping" / os.path.basename(vmap)
            zf.write(vmap, str(dest))

        # 7. Parameters
        if args.parameters:
            zf.write(args.parameters, str(base / "parameters.prop"))

        # 8. Security artifacts
        for sec in (args.security or []):
            dest = base / "security" / os.path.basename(sec)
            zf.write(sec, str(dest))

    size = os.path.getsize(output)
    print(f"Created iFlow ZIP: {output} ({size:,} bytes)")
    return 0


def cmd_template(args):
    """Generate a starter iFlow ZIP with example Groovy scripts."""
    name = args.name
    output = args.output or f"{name}-template.zip"
    base = Path("src/main/resources")

    with zipfile.ZipFile(output, "w", zipfile.ZIP_DEFLATED) as zf:
        # MANIFEST.MF
        manifest = MANIFEST_TEMPLATE.format(
            name=name, date=datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
        )
        zf.writestr("META-INF/MANIFEST.MF", manifest)

        # flow.xml
        zf.writestr(str(base / "flow.xml"), FLOW_TEMPLATE.format(name=name))

        # Example Groovy scripts
        zf.writestr(str(base / "script/transform.groovy"), GROOVY_TRANSFORM_TEMPLATE)
        zf.writestr(str(base / "script/format-response.groovy"), GROOVY_RESPONSE_TEMPLATE)

        # Example parameters
        zf.writestr(str(base / "parameters.prop"), PARAMETERS_TEMPLATE)

    size = os.path.getsize(output)
    print(f"Created iFlow template: {output} ({size:,} bytes)")
    print(f"  Contents:")
    with zipfile.ZipFile(output, "r") as zf:
        for info in zf.infolist():
            print(f"    {info.filename} ({info.file_size:,} bytes)")
    return 0


def cmd_validate(args):
    """Validate iFlow ZIP structure."""
    input_path = Path(args.input)
    if not input_path.exists():
        print(f"ERROR: {input_path} not found", file=sys.stderr)
        return 1

    if not zipfile.is_zipfile(input_path):
        print(f"ERROR: {input_path} is not a valid ZIP file", file=sys.stderr)
        return 1

    errors = []
    warnings = []

    with zipfile.ZipFile(input_path, "r") as zf:
        names = set(zf.namelist())

        # Check required files
        for required in REQUIRED_FILES:
            if required not in names:
                errors.append(f"Missing required file: {required}")

        # Check MANIFEST.MF content
        if "META-INF/MANIFEST.MF" in names:
            manifest_content = zf.read("META-INF/MANIFEST.MF").decode("utf-8")
            if "Bundle-SymbolicName:" not in manifest_content:
                errors.append("MANIFEST.MF: missing Bundle-SymbolicName")
            if "Bundle-Version:" not in manifest_content:
                warnings.append("MANIFEST.MF: missing Bundle-Version")

        # Check flow.xml is valid XML
        if "src/main/resources/flow.xml" in names:
            try:
                flow_content = zf.read("src/main/resources/flow.xml")
                ET.fromstring(flow_content)
            except ET.ParseError as e:
                errors.append(f"flow.xml: invalid XML — {e}")

        # Validate Groovy scripts have .groovy extension
        for name in names:
            if "script/" in name and not name.endswith(".groovy"):
                warnings.append(f"Script without .groovy extension: {name}")
            if "mapping/" in name:
                ext = os.path.splitext(name)[1]
                if ext not in (".xslt", ".mmap", ""):
                    warnings.append(f"Unrecognized mapping extension: {name}")

        # Check for empty files
        for info in zf.infolist():
            if info.file_size == 0:
                warnings.append(f"Empty file: {info.filename}")

    # Print report
    print(f"iFlow ZIP validation: {input_path}")
    print(f"  Files: {len(names)}")
    print(f"  Required OK: {sum(1 for r in REQUIRED_FILES if r in names)}/{len(REQUIRED_FILES)}")

    if errors:
        print(f"\n  ERRORS ({len(errors)}):")
        for e in errors:
            print(f"    [ERR] {e}")
    if warnings:
        print(f"\n  WARNINGS ({len(warnings)}):")
        for w in warnings:
            print(f"    [WARN] {w}")

    if not errors:
        print(f"\n  PASS: iFlow ZIP is VALID")
        return 0
    else:
        print(f"\n  FAIL: iFlow ZIP has {len(errors)} error(s)")
        return 1


def cmd_extract(args):
    """Extract iFlow ZIP to directory (path-traversal safe)."""
    input_path = Path(args.input)
    output_dir = Path(args.output).resolve()
    output_dir.mkdir(parents=True, exist_ok=True)

    with zipfile.ZipFile(input_path, "r") as zf:
        for info in zf.infolist():
            # Prevent zip-slip path traversal
            member_path = os.path.normpath(output_dir / info.filename)
            if not str(member_path).startswith(str(output_dir)):
                print(f"WARNING: Skipping suspicious path: {info.filename}", file=sys.stderr)
                continue
            zf.extract(info, output_dir)

    print(f"Extracted {input_path} -> {output_dir}")
    print(f"  Files extracted: {len(zf.namelist())}")
    return 0


def cmd_list(args):
    """List contents of iFlow ZIP."""
    input_path = Path(args.input)

    with zipfile.ZipFile(input_path, "r") as zf:
        print(f"iFlow ZIP: {input_path}")
        print(f"  Total files: {len(zf.infolist())}")
        print(f"  Total size:  {sum(i.file_size for i in zf.infolist()):,} bytes")
        print()

        # Group by directory
        dirs = {}
        for info in zf.infolist():
            d = os.path.dirname(info.filename) or "/"
            dirs.setdefault(d, []).append(info)

        for d, files in sorted(dirs.items()):
            print(f"  {d}/")
            for f in files:
                print(f"    {os.path.basename(f.filename):40s} {f.file_size:>8,} bytes")
    return 0


