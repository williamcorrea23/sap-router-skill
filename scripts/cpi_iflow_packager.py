#!/usr/bin/env python3
"""
CPI iFlow Packager — create, validate, and extract SAP CPI integration flow ZIP files.

CPI iFlow ZIP structure:
  META-INF/MANIFEST.MF              — Bundle metadata
  src/main/resources/flow.xml        — Main integration flow descriptor
  src/main/resources/script/*.groovy  — Groovy scripts
  src/main/resources/mapping/*.xslt   — XSLT mappings
  src/main/resources/mapping/*.mmap   — Message mappings
  src/main/resources/resources/*     — EDI schemas, WSDLs, etc.
  src/main/resources/value-mapping/*.vmap  — Value mappings
  src/main/resources/parameters.prop — Externalized parameters

Usage:
  python scripts/cpi_iflow_packager.py create --name <id> --flow <file>
        [--scripts <files>] [--mappings <files>] [--resources <dir>]
        [--parameters <file>] [--output <file.zip>]

  python scripts/cpi_iflow_packager.py validate --input <file.zip>

  python scripts/cpi_iflow_packager.py extract --input <file.zip> --output <dir>

  python scripts/cpi_iflow_packager.py list --input <file.zip>

  python scripts/cpi_iflow_packager.py template --name <id> --output <file.zip>
    Generate a starter iFlow ZIP with minimal flow.xml + example Groovy script.
"""

